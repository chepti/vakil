// מנקה גרשיים ומאחד רווחים
function normalize(s) {
  return (s || '').replace(/["'״׳]/g, '').replace(/\s+/g, ' ').trim()
}

/**
 * מאתר קואורדינטות בתוך טקסט חופשי. מחזיר { lat, lng, name } או null.
 * בוחר את שם המקום הארוך ביותר שמופיע כ-substring (כדי ש"מעלה אדומים"
 * יזכה לפני "אדומים", ו"קרית משה" יזכה לפני "ירושלים" אם שניהם מופיעים).
 */
export function geocodeCity(text) {
  const t = normalize(text)
  if (!t) return null

  let best = null
  for (const [name, coords] of Object.entries(PLACES)) {
    const n = normalize(name)
    if (t.includes(n)) {
      if (!best || n.length > best.matchLen) {
        best = { lat: coords[0], lng: coords[1], name, matchLen: n.length }
      }
    }
  }
  if (!best) return null
  return { lat: best.lat, lng: best.lng, name: best.name }
}
